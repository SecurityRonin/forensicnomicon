/// Returns the combined profile for a given artifact ID, or `None` if unknown.
///
/// # Example
///
/// ```rust
/// use forensicnomicon::profile::profile_for;
/// use forensicnomicon::evidence::EvidenceStrength;
/// use forensicnomicon::volatility::VolatilityClass;
///
/// let p = profile_for("mft").unwrap();
/// assert_eq!(p.evidence_strength, EvidenceStrength::Definitive);
/// assert_eq!(p.volatility, VolatilityClass::Persistent);
/// ```
pub fn profile_for(artifact_id: &str) -> Option<&'static ArtifactProfile> {
    ARTIFACT_PROFILES.iter().find(|p| p.id == artifact_id)
}
