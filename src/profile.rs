//! Combined artifact profile — evidence strength + volatility in one place.
//!
//! [`ARTIFACT_PROFILES`] is the single authoritative table that merges
//! [`crate::evidence::EVIDENCE_TABLE`] and [`crate::volatility::VOLATILITY_TABLE`].
//! Adding a new artifact requires only one edit here.
//!
//! Use [`profile_for`] for point-lookups, or iterate [`ARTIFACT_PROFILES`] for
//! bulk operations.

use crate::evidence::EvidenceStrength;
use crate::volatility::VolatilityClass;

/// Combined profile: evidence weight + acquisition urgency for one artifact.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
#[non_exhaustive]
#[cfg_attr(feature = "serde", derive(serde::Serialize))]
pub struct ArtifactProfile {
    /// Catalog artifact ID (same key used in [`crate::catalog::CATALOG`]).
    pub id: &'static str,
    /// How strongly this artifact proves a fact in isolation.
    pub evidence_strength: EvidenceStrength,
    /// Known caveats, edge cases, or false-positive scenarios.
    pub evidence_caveats: &'static [&'static str],
    /// How quickly this artifact is overwritten or lost (RFC 3227 class).
    pub volatility: VolatilityClass,
    /// One-line rationale for the volatility classification.
    pub volatility_rationale: &'static str,
}

